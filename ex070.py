# 2. 원본 리스트는 그래도 두고 새롭게 정렬된 리스트를 생성하는 방법
data = [2, 4, 3, 1, 5, 10, 9]
# sorted() 함수에 data라는 리스트를 입력해준다. 정렬된 리스트를 data2라는 새로운 메모리에 입력해준다.
data2 = sorted(data)
# 새롭게 정렬된 data2를 출력한다.
print(data2)

# 실행결과 : [1, 2, 3, 4, 5, 9, 10] 

