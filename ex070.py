# 1. 원본 리스트를 바로 변경하는 방법
data = [2, 4, 3, 1, 5, 10, 9]
# sort() 함수를 사용하여 리스에 있는 값을 오름차순으로 정렬한다. 
data.sort()
# 정렬된 data값을 출력한다.
print(data)

# 실행결과 : [1, 2, 3, 4, 5, 9, 10] 


